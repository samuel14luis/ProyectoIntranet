    private void abrirMenu() {
        jpaneMenu.grabFocus();
        int pos = jpaneMenu.getX();
        if (pos < -1) {
            Animacion.Animacion.mover_derecha(10 - jpaneMenu.getWidth(), 0, 2, 2, jpaneMenu);
        }
    }

    private void cerrarMenu() {
        int xframe, xmouse;
        xmouse = java.awt.MouseInfo.getPointerInfo().getLocation().x;
        xframe = getX();
        int pos = jpaneMenu.getX();
        if (pos > -1 && (xmouse - xframe) > jpaneMenu.getWidth()) {
            Animacion.Animacion.mover_izquierda(0, 10 - jpaneMenu.getWidth(), 2, 3, jpaneMenu);
        }
    }

    private void mouseOverItem(JPanel pane, JPanel active, JLabel label) {
        mouseOverItem(pane, active, label, null);
    }

    private void mouseOverItem(JPanel pane, JPanel active, JLabel label, String icon) {
        abrirMenu();
        pane.setBackground(AtributosGUI.item_mouseOver_panel);
        if (icon != null) {
            jlblPerfil.setIcon(new javax.swing.ImageIcon(getClass().getResource(icon)));
        }
    }

    private void mouseExitedItem(JPanel pane, JPanel active, JLabel label) {
        mouseExitedItem(pane, active, label, null);
    }

    private void mouseExitedItem(JPanel pane, JPanel active, JLabel label, String icon) {
        cerrarMenu();
        pane.setBackground(AtributosGUI.color_principal);
        label.setForeground(AtributosGUI.item_mouseExited_label_foreground);
        if (icon != null) {
            jlblPerfil.setIcon(new javax.swing.ImageIcon(getClass().getResource(icon)));
        }
    }

